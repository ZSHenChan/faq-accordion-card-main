function answer1(){
    const dropbox = document.getElementById("answer-1");
    const ques = document.getElementById("question-1");
    if (dropbox.style.display=="none"){
        dropbox.style.display="flex";
        ques.style.color = "hsl(238, 29%, 16%)";
        ques.style.fontWeight = "700";
    }
    else{
        dropbox.style.display="none";
        ques.style.fontWeight="400";
    }
}

